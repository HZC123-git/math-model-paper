h2("5.2 模型的缺点")

para("(1)特征维度有限。当前模型仅包含四个自变量，远未覆盖住宅特征的全部谱系。在特征价格理论的综合框架下，住宅特征通常分为建筑特征、区位特征和邻里特征三大类。当前模型仅覆盖了部分建筑特征和一个邻里特征，大量影响房价的变量未被纳入。缺失变量可能导致的后果是：R2被低估，或者如果缺失变量与已纳入变量相关，则OLS系数估计将存在遗漏变量偏误(Omitted Variable Bias)。")

para("(2)样本代表性问题。500条交易记录虽然覆盖了较广的价格区间，但其地理范围(单一城市或区域)、时间跨度(可能集中在某一时段)和产品类型(可能以普通住宅为主)未在数据中明确标注。房地产市场的强地域异质性意味着：在一个城市拟合的模型，直接移植到另一个城市可能产生严重的预测偏差。")

para("(3)未检验交互效应和非线性关系。当前模型的加法结构隐含假设了\"各特征的边际效应相互独立\"。这一假设在现实中可能不完全成立——学区溢价可能随面积增大而放大，房龄对价格的折损率可能随房龄增加而递减。")

para("(4)时间维度的缺失。房地产市场具有强周期性。当前模型使用的是截面数据，无法捕捉房价随时间波动的动态规律，也无法预测未来房价的走势。")

h2("5.3 模型改进与推广")

para("在特征层面，引入更多变量以提高模型的完备性：增加楼层、朝向、装修标准、有无车位；增加距最近地铁站的距离、距CBD的通勤时间；将学区属性从二值变量细化为连续变量(学校排名百分位或升学率)。在方法论层面，检验并引入交互项——面积x学区、面积x房龄——来捕捉特征间的协同或抑制效应。使用VIF(方差膨胀因子)来系统性地诊断新增变量是否引入共线性问题。在数据层面，增加样本量至2000条以上，扩展数据的地理覆盖范围至多个城市，引入多期数据以加入时间固定效应控制市场周期的波动。")

