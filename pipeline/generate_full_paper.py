h1("四、模型建立与求解")
h2("4.1 问题分析")

para("本章的核心任务是在给定四个特征变量的条件下，构建并选择最优的房价预测模型。涉及三个需要慎重权衡的决策点。第一，线性与非线性模型的选择。特征价格理论假设房价是各项特征的线性函数，但现实中的交互效应和非线性关系可能偏离该假设。本文不预设结论，而是将五类模型置于同一数据集上公平对比。第二，模型复杂度与泛化能力的权衡。OLS模型仅需估计5个参数(n=500)，参数-样本比约为1:100，估计条件高度宽松；随机森林的有效参数数量远超OLS，可能在中小样本下过拟合。本文使用5折交叉验证衡量泛化能力。第三，特征重要性的可靠度量。随机森林特征重要性(基于基尼不纯度减少)和线性回归标准化系数(基于边际效应)两种独立方法同时使用，交叉验证特征排序的稳健性。")

h2("4.2 数据预处理与实验设置")

para("数据划分。将500条数据按80:20的比例随机划分为训练集(392条)和测试集(98条)，设置random_state=42以确保可复现性。测试集在整个建模过程中完全隔离，仅在最终评估阶段使用。标准化处理。使用StandardScaler对X进行z-score标准化：x_std=(x-mu)/sigma，标准化参数仅在训练集上计算后原样应用于测试集。评价指标体系。在测试集上统一使用R2、RMSE和MAE三项指标。R2衡量方差解释比例，RMSE对大误差敏感(平方放大)，MAE对异常值不敏感。此外对每个模型执行5折交叉验证，记录CV R2的均值和标准差。")

h2("4.3 多元线性回归(OLS)")

para("多元线性回归是特征价格理论的标准计量工具，模型形式为y=B0+B1x1+B2x2+B3x3+B4x4+epsilon。使用普通最小二乘法(OLS)估计参数，最小化残差平方和。LinearRegression在测试集上的表现为：R2=0.9568，RMSE=14.623万元，MAE=11.584万元。模型解释了房价95.68%的方差，RMSE在414万元均价下代表约3.53%的相对误差。5折交叉验证R2均值为0.9495，标准差0.0139。")

h2("4.4 Ridge回归(L2正则化)")

para("Ridge回归在OLS的损失函数上增加L2惩罚项，对所有权重进行均匀收缩但不设为零。本文设定alpha=1.0。Ridge在测试集上的表现为：R2=0.9567，RMSE=14.630万元，MAE=11.590万元。与OLS的差异微乎其微(R2下降0.0001)，5折交叉验证R2均值为0.9495(标准差0.0139)。小幅度的性能下降是正则化的正常代价——在不存在共线性时，无偏估计(OLS)确实是均方误差意义上的最优选择。这一结果从侧面验证了数据中不存在多重共线性问题。")

h2("4.5 Lasso回归(L1正则化)")

para("Lasso回归将L2惩罚替换为L1惩罚，其几何特性使得最优解倾向于落在坐标轴上，部分系数被压缩至零，因此Lasso同时完成系数估计和变量选择。本文设定alpha=0.01。Lasso在测试集上的表现为：R2=0.9568，RMSE=14.623万元，MAE=11.584万元。关键发现：没有系数被压缩至零。四个自变量在Lasso的变量选择步骤中全部被保留，说明每个特征都有独立的、不可被其他特征完全替代的贡献。三种线性模型在测试集上的R2差距不超过0.0001，说明OLS就是最优选择。")

h2("4.6 随机森林")

para("随机森林基于Bagging思想，在Bootstrap采样的训练子集上独立训练多棵决策树，每棵树在分裂时只考虑随机选取的部分特征，通过所有树的预测值取平均输出。本文设定n_estimators=100，max_depth=10。随机森林在测试集上的表现为：R2=0.9161，RMSE=20.380万元，MAE=16.217万元。相比OLS，R2下降了0.0407，RMSE增加了5.757万元(约39%的相对增幅)。5折交叉验证R2均值为0.9009(标准差0.0278)，标准差是线性模型的2倍。这种退化说明随机森林在500条数据上确实存在过拟合。")

h2("4.7 梯度提升树")

para("梯度提升树串行地训练一系列弱学习器，每棵新树拟合前面所有树累积的残差。本文设定n_estimators=100，max_depth=5。梯度提升树在测试集上的表现为：R2=0.9162，RMSE=20.367万元，MAE=16.416万元。与随机森林的表现几乎完全一致(R2仅高出0.0001)。两种核心策略完全不同的非线性模型在相同数据上得到高度一致的(偏低的)表现，这强化了一个判断：在500条数据、4个特征的前提下，房价与特征之间以线性关系为主，非线性模型没有可捕获的额外信号。")

