P("房价决定理论最早可追溯至Rosen(1974)的特征价格理论(Hedonic Price Theory)，其核心思想是：异质性商品的价格可以被分解为其各项特征的边际价格之和。在住宅市场中，房屋被视为一组特征的集合——面积、户型、楼层、朝向、装修、房龄、学区、交通、商业配套等——每一类特征对应一个隐含价格，所有特征价格加总即为房屋总价。该理论框架为房价建模提供了清晰的计量路径：将房价对各项特征做回归，回归系数即为该特征的单位边际贡献。大量实证研究支持了该框架在住宅定价中的有效性(Malpezzi, 2003; Sirmans et al., 2005)。")

P("然而在实际建模中面临三个关键决策。第一，特征之间的相关性——面积与卧室数通常正相关(大面积住宅往往卧室更多)，如果相关性过强会导致回归系数估计不稳定，即多重共线性问题。第二，线性与非线性的选择——部分特征对房价的影响可能不是简单的线性叠加，例如学区的溢价效应可能随面积增大而放大(交互效应)，或者房龄对价格的折损率可能随房龄增加而递减(非线性关系)。第三，样本量与模型复杂度的平衡——集成学习模型在特征维度高、样本量充足时表现优异，但在中小样本下容易过拟合，反而不如简单的线性模型。")

P("本研究使用的数据集包含500组住宅交易记录，涵盖面积、卧室数、房龄、学区属性四个特征和成交总价一个目标变量。数据量适中，特征维度较低，这为系统性地对比线性方法与非线性方法创造了条件：如果线性假设成立，则OLS以最少的参数获得最优的泛化效果；如果数据中存在显著的非线性模式，则集成方法应展现出明显优势。本研究不同于传统方法的是，将模型对比从5种扩展至12种四类，覆盖线性、树、核方法和神经网络，以更全面的视角回答'何种模型最优'的问题。")

