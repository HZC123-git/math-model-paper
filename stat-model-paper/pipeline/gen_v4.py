H2("5.2 模型的缺点")

P("(1)特征维度有限。当前模型仅包含四个自变量，远未覆盖住宅特征的全部谱系。在特征价格理论的综合框架下，住宅特征通常分为三大类——建筑特征(面积、房龄、楼层、朝向、装修标准、车位配比等)、区位特征(距CBD距离、距地铁距离、周边商业配套密度等)和邻里特征(学区质量、治安水平、绿地率、容积率等)(Sirmans et al., 2005)。当前模型仅覆盖了部分建筑特征和一个邻里特征，大量影响房价的变量未被纳入。可能的后果是——R2被低估(如果有更多特征可以解释剩余方差)，或者更严重的——如果缺失变量与已纳入变量存在相关性，OLS系数估计将存在遗漏变量偏误(Omitted Variable Bias)。")

P("(2)样本代表性问题。500条交易记录虽覆盖了较广的价格区间(234-682万元)，但其地理范围(单一城市或区域)、时间跨度(可能集中在某一时段)和产品类型(可能以普通住宅为主)未在数据中明确标注。房地产市场的强地域异质性意味着——一个城市拟合的模型直接移植到另一城市可能产生严重的预测偏差。例如一线城市学区溢价可达到总价的30%，而三四线城市可能不足10%。")

P("(3)未检验交互效应和非线性关系。当前模型的加法结构(y=B0+sum(Bi*xi))隐含假设了'各特征的边际效应相互独立'——即学区溢价与房屋面积无关，房龄的折旧率与是否学区无关。这一假设在现实中可能不完全成立——学区溢价可能随面积增大而放大(交互效应)，房龄对价格的折损率可能随房龄增加而递减(非线性)。SVR-rbf的完全失效(0.5254)似乎支持了线性假设，但这不等于'不存在任何交互效应'，可能只是交互效应信号太弱，不足以在整体R2中体现。")

P("(4)时间维度的缺失。房地产市场具有强周期性——上涨周期中买家预期乐观，成交价可能系统性高于模型预测；调控收紧期成交价可能系统性低于模型预测。当前模型使用截面数据(同一时点的500条交易)，无法捕捉房价随时间波动的动态规律，也无法预测未来走势。")

H2("5.3 模型改进与推广")

P("在特征层面，引入更多变量以提高模型完备性——建筑特征方面增加楼层(低/中/高)、朝向(南/非南)、装修标准(毛坯/简装/精装/豪装)、有无电梯和车位；区位特征方面增加距最近地铁站距离、距CBD通勤时间和周边1km内商业设施数量；邻里特征方面将学区从二值变量细化为连续变量(学校排名百分位或升学率)，并增加小区容积率、绿化率和治安案件率等变量。")

P("在方法论层面，检验并引入交互项——面积×学区、面积×房龄、学区×房龄——捕捉特征间的协同或抑制效应。使用VIF(方差膨胀因子)系统性诊断新增变量是否引入共线性问题。在数据层面，增加样本量至2000条以上，扩展地理覆盖至多个城市或区域，引入多期数据(季度或年度面板)以加入时间固定效应控制市场周期波动。")

